// Build a lookup map: sorted-group-key (e.g. "ABCDEFGH") → match→group mapping
const ASSIGNMENT_MAP: Map<string, Record<number, string>> = (() => {
  const map = new Map<string, Record<number, string>>()
  for (const option of ASSIGNMENT_OPTIONS) {
    const key = option.split('').sort().join('')
    const matches: Record<number, string> = {}
    for (let i = 0; i < W3_MATCH_ORDER.length; i++) {
      matches[W3_MATCH_ORDER[i]] = option[i]
    }
    map.set(key, matches)
  }
  return map
})()

/**
 * Given the 8 group letters whose third-placed teams qualify, return a
 * mapping from R32 match number (74, 77, 79, 80, 81, 82, 85, 87) to the
 * group letter whose third-placed team plays in that match.
 *
 * Returns null if the input is not exactly 8 unique group letters from A-L.
 */
export function getThirdPlaceAssignment(qualifyingGroups: string[]): Record<number, string> | null {
  if (qualifyingGroups.length !== 8) return null
  const unique = new Set(qualifyingGroups)
  if (unique.size !== 8) return null
  const key = [...qualifyingGroups].sort().join('')
  return ASSIGNMENT_MAP.get(key) ?? null
}
